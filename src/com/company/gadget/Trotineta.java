package com.company.gadget;

public class Trotineta extends Gadget {
    private int kw;
    private int distanta;
    private int viteza;
    private int persoane;


    public int getKw() {
        return kw;
    }

    public void setKw(int kw) {
        this.kw = kw;
    }

    public int getDistanta() {
        return distanta;
    }

    public void setDistanta(int distanta) {
        this.distanta = distanta;
    }

    public int getViteza() {
        return viteza;
    }

    public void setViteza(int viteza) {
        this.viteza = viteza;
    }

    public int getPersoane() {
        return persoane;
    }

    public void setPersoane(int persoane) {
        this.persoane = persoane;
    }
}
